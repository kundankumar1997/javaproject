    private void facilitypanel_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_facilitypanel_buttonActionPerformed
        PackageFac.feature.clear();

        if (ac_bus_cb.isSelected()) {
            PackageFac.feature.add("AC BUS");
        }
        if (ac_hotel_cb.isSelected()) {
            PackageFac.feature.add("AC HOTEL");
        }
        if (p_vehicle_cb.isSelected()) {
            PackageFac.feature.add("PERSONAL VEHICLE");
        }
        if (amusement_cb.isSelected()) {
            PackageFac.feature.add("AMUSEMENT");
        }
        PackageFac.total_amount=Integer.parseInt(tp_label.getText());
       
    }//GEN-LAST:event_facilitypanel_buttonActionPerformed


