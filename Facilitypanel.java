    private void ac_hotel_cbItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ac_hotel_cbItemStateChanged
        if (ac_hotel_cb.isSelected() == true) {
            facPrice += 800;
        } else {
            facPrice -= 800;
        }
        totalextrapric.setText("" + facPrice);
        tp_label.setText("" + (facPrice + PackageFac.pricecombo));
        
    }//GEN-LAST:event_ac_hotel_cbItemStateChanged

